public class FloatDatail {
	public static void main(String[] aegs) {
		//java的浮点型常量（具体值）默认为double型，声明float型常量
		//必须后家‘f’或’F‘
		float num2 = 1.1F;
		double num3 = 2.2;
		//通常情况下使用double型，因为他比float型更精确。
		double num9 = 2.23445464435;
		float num10 = 2.32435323252F;
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num9);
		System.out.println(num10);
		}
}